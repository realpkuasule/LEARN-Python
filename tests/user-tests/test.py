def 施放魔法(施放者, 目标, 法术名称="火球术", 魔法强化=False):
    """施放魔法——默认是火球术，默认不强化"""
    # 魔法书——每个魔法的消耗和基础效果
    魔法书 = {
        "火球术":  {"mp消耗": 10, "基础伤害": 30},
        "暴风雪":  {"mp消耗": 25, "基础伤害": 80},
        "治愈术":  {"mp消耗": 15, "基础治疗": 50},
    }
    选定魔法 = 魔法书[法术名称]

    # 检查 MP 够不够
    if 施放者["mp"] < 选定魔法["mp消耗"]:
        print(f"MP 不足！需要 {选定魔法['mp消耗']}，当前只有 {施放者['mp']}")
        return   # MP 不够，直接结束函数

    施放者["mp"] -= 选定魔法["mp消耗"]

    # 伤害型魔法
    if "基础伤害" in 选定魔法:
        伤害 = 选定魔法["基础伤害"]
        if 魔法强化:
            伤害 = 伤害 * 1.5   # 魔力强化——伤害 ×1.5
        目标["hp"] -= 伤害
        魔法强化文本 = "（魔力强化！）" if 魔法强化 else ""
        print(f"{施放者['名字']} 释放 {法术名称}！造成 {伤害} 点伤害{魔法强化文本}")

    # 治疗型魔法
    elif "基础治疗" in 选定魔法:
        治疗值 = 选定魔法["基础治疗"]
        目标["hp"] = min(目标["hp"] + 治疗值, 目标.get("最大hp", 目标["hp"] + 治疗值))
        print(f"{施放者['名字']} 释放 {法术名称}！恢复 {治疗值} 点 HP")

# 创建法师和怪物
法师 = {"名字": "大法师 子房", "hp": 60, "mp": 100, "攻击力": 5}
boss = {"名字": "恶龙", "hp": 500, "攻击力": 80, "防御力": 30, "最大hp": 500}

# 四种调用方式——对应四种参数传法
施放魔法(法师, boss)                          # 1. 全用默认：火球术、不强化
施放魔法(法师, boss, "暴风雪")                 # 2. 指定魔法名（位置参数）
施放魔法(法师, boss, "火球术", True)           # 3. 按位置全传
施放魔法(法师, boss, 魔法强化=True)             # 4. 关键字参数：跳过 法术名称，直接用默认火球 + 强化